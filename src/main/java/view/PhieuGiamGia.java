    private void showDetai() {
        vc = lstKM.get(index);
        txtMaKM.setText(vc.getMaVoucher());
        txtGiaTriGiam.setText(String.valueOf(vc.getGiaTri()));
        txtSoluong.setText(String.valueOf(vc.getSoLuong()));
        txtMoTa.setText(vc.getMoTa());
        if (vc.getTrangThai().equals("Đã kết thúc")) {
            cboTT.setSelectedIndex(0);
        } else {
            cboTT.setSelectedIndex(1);
        }
        txtTenKM.setText(vc.getTenVoucher());
        java.sql.Date dateStringDB = vc.getNgayBatdau();
        jdcDB.setDate(dateStringDB);
        java.sql.Date dateStringKT = vc.getNgayKetthuc();
        jdcKT.setDate(dateStringKT);

        tblBangKM.setRowSelectionInterval(index, index);
    }
