    private void LoadDataToTable() throws Exception {
        lstKM = vcs.getAllKhuyenMai();
        model.setRowCount(0);
        for (model.PhieuGiamGia voucher : lstKM) {
            model.addRow(new Object[]{
                voucher.getMaVoucher(),
                voucher.getTenVoucher(),
                voucher.getGiaTri(),
                voucher.getNgayBatdau(),
                voucher.getNgayKetthuc(),
                voucher.getSoLuong(),
                voucher.getMoTa(),
                voucher.getTrangThai()
            });
        }
        String[] col = {
            "Mã PGG", "Tên PGG", "Giá trị giảm", "Ngày bắt đầu", "Ngày kết thúc", "Số lượng", "Mô tả", "Trạng thái"
        };
        model.setColumnIdentifiers(col);
    }
