    private boolean checkMa(String mavc) {
        for (model.PhieuGiamGia voucher : lstKM) {
            if (voucher.getMaVoucher().equalsIgnoreCase(mavc)) {
                return true;
            }
        }
        return false;
    }

    private void createKhuyenMai() throws NumberFormatException {
        vc = new model.PhieuGiamGia();
        vc.setMaVoucher(txtMaKM.getText().trim());
        vc.setTenVoucher(txtTenKM.getText().trim());
        vc.setSoLuong(Integer.parseInt(txtSoluong.getText().trim()));
        vc.setGiaTri(Float.parseFloat(txtGiaTriGiam.getText().trim()));
        java.util.Date utilDate = jdcDB.getDate();
        java.util.Date utilDate2 = jdcKT.getDate();
        if (utilDate != null) {
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            vc.setNgayBatdau(sqlDate);
        }
        if (utilDate2 != null) {
            java.sql.Date sqlDate2 = new java.sql.Date(utilDate2.getTime());
            vc.setNgayKetthuc(sqlDate2);
        }
        vc.setTrangThai((String) cboTT.getSelectedItem());
        vc.setMoTa(txtMoTa.getText().trim());
    }

    private boolean checkEmpry() {
        StringBuilder sb = new StringBuilder();

        if (txtTenKM.getText().equals("")) {
            sb.append("Vui lòng nhập vào tên khuyến mại !\n");
        }
        if (txtMaKM.getText().equals("")) {
            sb.append("Vui lòng nhập vào mã khuyễn mại !\n");
        }
        int check = 0;
        try {
            if (txtSoluong.getText().equals("")) {
                sb.append("Vui lòng nhập vào số lượng áp dụng !\n");
            } else if (txtSoluong.equals(check));
        } catch (Exception e) {
            sb.append("Số lượng phải là số !\n");
        }
        if (jdcDB.getDate() == null) {
            sb.append("Vui lòng chọn ngày bắt đầu !\n");
        }
        if (jdcKT.getDate() == null) {
            sb.append("Vui lòng chọn ngày kết thúc !\n");
        }
        if (txtMoTa.getText().equals("")) {
            sb.append("Vui lòng nhập vào mô tả !\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return true;
        }
        return false;
    }
