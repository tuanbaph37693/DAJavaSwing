    private void filterByRole(String selectedRole) throws Exception {
        DefaultTableModel model = (DefaultTableModel) tblBangKM.getModel();
        model.setRowCount(0);

        for (model.PhieuGiamGia voucher : lstKM) {
            String tt = voucher.getTrangThai();

            if ((selectedRole.equals("Đã kết thúc") && tt.equals("0"))
                    || (selectedRole.equals("Còn hiệu lực") && tt.equals("1"))) {

                tt = (tt.equals("0")) ? "Đã kết thúc" : "Còn hiệu lực";

                model.addRow(new Object[]{
                    voucher.getMaVoucher(), voucher.getTenVoucher(), voucher.getGiaTri(), voucher.getSoLuong(),
                    voucher.getNgayBatdau(), voucher.getNgayKetthuc(),
                    tt,
                    voucher.getMoTa()
                });
            }
        }
    }

