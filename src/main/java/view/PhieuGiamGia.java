    private void ConHieuLuc() {
        try {
            lstKM = vcs.getAllKhuyenMai();
        } catch (Exception ex) {
            Logger.getLogger(PhieuGiamGia.class.getName()).log(Level.SEVERE, null, ex);
        }
        model.setRowCount(0);
        for (model.PhieuGiamGia voucher : lstKM) {
            // Check if the Trangthai is "Đã kết thúc"
            if (!voucher.getTrangThai().equals("Đã kết thúc")) {
                model.addRow(new Object[]{
                    voucher.getMaVoucher(),
                    voucher.getTenVoucher(),
                    voucher.getGiaTri(),
                    voucher.getNgayBatdau(),
                    voucher.getNgayKetthuc(),
                    voucher.getSoLuong(),
                    voucher.getMoTa(),
                    voucher.getTrangThai()
                });
            }
        }
    }

