    private boolean LoginsuccesFully() {
        try {
            txtTenNhanVien.setText(SAVENHANVIEN.luuNguoiDung.getHoTen());
            if (SAVENHANVIEN.luuNguoiDung.getVaiTro() == 0) {
                txtTypeAccount.setText("NHÂN VIÊN");
            } else if (SAVENHANVIEN.luuNguoiDung.getVaiTro() == 1) {
                txtTypeAccount.setText("QUẢN LÍ");
            }
            return true;
        } catch (Exception e) {
            System.out.println("Lỗi  " + e);
            return false;

        }

    }

