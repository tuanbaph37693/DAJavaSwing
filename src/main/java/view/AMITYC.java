    private void lblThongKeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblThongKeMouseClicked
        try {
            TabPanN_AllChucNang.removeAll();
            int check = TabPanN_AllChucNang.getComponentCount();
            if (!tabAdded) {
                tk = new ThongKe();
                TabPanN_AllChucNang.addTab("THỐNG KÊ", tk);
                TabPanN_AllChucNang.setSelectedComponent(tk);
                tabAdded = true; // Đánh dấu đã thêm tab
            } else {
                // Nếu đã thêm tab và số lượng tab trở lại 0, reset lại biến tabAdded
                if (TabPanN_AllChucNang.getTabCount() == check) {
                    tabAdded = false;
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_lblThongKeMouseClicked

